package Models;

/**
 *
 * @author mim
 */
public class Schedule {
    private int id;
    private String day;
    private int time;

    public Schedule(int id, String day, int time) {
        this.id = id;
        this.day = day;
        this.time = time;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    

    public Schedule() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
    
}
